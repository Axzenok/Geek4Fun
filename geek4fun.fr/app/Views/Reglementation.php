            <p><h2>Règlementation</h2></p>
            <p><h3>Inscriptions</h3></p>

            <p>Les participants doivent s'inscrire en ligne sur le site du festival: geek4fun.fr/cosplay.</p>

            <p>Les inscriptions sont ouvertes trois mois avant le festival et se poursuivent jusqu'au
                jour de l'événement, sous réserve de disponibilité.</p>

            <p>Le concours est ouvert aux participants de 18 ans et plus.</p>

            <p>Chaque participant doit se pré-inscrire via le formulaire en ligne, fournissant une image
                de référence de bonne qualité de son personnage et une bande-son pour accompagner sa présentation.</p>

            <p>Les participants doivent avoir réalisé au moins 80% de leur costume.
                Les cosplays achetés non signalés entraîneront la disqualification.</p>

            <p><h3>Concours</h3></p>

            <p>Les participants confirmés doivent se présenter avant 12h au stand
                "inscriptions cosplay" le jour du festival pour valider leur inscription.</p>

            <p>Les participants passeront individuellement devant le jury selon les
                horaires communiqués par mail.</p>

            <p>Les costumes doivent appartenir aux thèmes suivants : Jeux vidéo, Dessins animés,
                Films ou séries, Animés, Mangas, BD, Comics, Littérature.</p>

            <p>Des prix seront attribués le vendredi, le samedi, et le dimanche, comprenant le Coup de
                Cœur du Public et du Jury.</p>

            <p><h3>Loges et Matériel</h3></p>

            <p>Des loges équipées sont accessibles aux participants avec du matériel de réparation disponible.</p>

            <p>Certains objets et comportements sont strictement interdits, tandis que d'autres, tels que
                les répliques d'armes sans danger, sont autorisés.</p>

            <p><h3>Règles de Sécurité</h3></p>

            <p>Armes blanches, armes à feu, liquides inflammables, produits salissants.</p>

            <p>Répliques d'armes sans danger, objets non apparentés à des armes.</p>

            <p><h3>Savoir-Vivre et Droit à l'Image</h3></p>

            <p>Les participants doivent adhérer à la charte du festival, et certains comportements
                sont strictement interdits.</p>

            <p>Les participants cèdent les droits d'exploitation de leur image.</p>

            <p><h3>Responsabilité et Informations Générales</h3></p>

            <p>L'organisateur n'est pas responsable du vol, de la détérioration des costumes, ou des accidents.</p>

            <p>Le festival se réserve le droit d'annuler ou de modifier le concours en
                cas de nécessité, sans possibilité de recours.</p>

            <p><h3>Inscription au Concours</h3></p>

            <p>Informations Attendues: Nom, prénom, pseudonyme, date de naissance, adresse, téléphone, email.</p>

            <p>Les participants confirment leur engagement à respecter le règlement et la charte du respect.</p>

            <p><h3>Charte du Respect</h3></p>

            <p>Le Festival Geek4Fun prône un environnement inclusif, respectueux et agréable pour tous les participants,
                visiteurs et membres du personnel. La Charte du Respect vise à établir des lignes directrices claires pour
                garantir une expérience positive pendant le concours de cosplay. En participant à l'événement, chaque individu
                s'engage à respecter les principes suivants :</p>

            <p><h3>Respect des Participants</h3></p>

            <p>Nous accueillons la diversité et respectons toutes les identités, quelles qu'elles soient.
                Aucune forme de discrimination basée sur le genre, l'orientation sexuelle, l'origine ethnique,
                la religion, ou tout autre critère ne sera tolérée.</p>

            <p>Chaque participant mérite d'être traité avec respect et dignité. Les commentaires,
                gestes ou comportements offensants, haineux ou discriminatoires envers les autres participants
                sont strictement interdits.</p>

            <p><h3>Respect de l'Événement</h3></p>

            <p>Nous apprécions un environnement propre et ordonné. Il est interdit de jeter des déchets ou
                d'endommager les installations.</p>

            <p>Les participants au concours de cosplay doivent respecter les thèmes définis.
                Tout costume ou comportement qui ne correspond pas aux thèmes énoncés sera refusé.</p>

            <p><h3>Respect de la Sécurité</h3></p>

            <p>Les participants doivent respecter les règles de sécurité en ce qui
                concerne les armes et accessoires. Aucune arme réelle, dangereuse, ou toute autre objet nuisible à la
                sécurité n'est autorisé.</p>

            <p>Tout geste, acte ou propos incitant à la violence, à la haine,
                ou insultant est strictement interdit. Les participants doivent maintenir un comportement
                respectueux envers les autres.</p>

            <p><h3>Respect des Résultats et Décisions</h3></p>

            <p>Les participants s'engagent à respecter les décisions du jury du concours de cosplay,
                y compris le classement établi et les commentaires fournis.</p>

            <p>Le fair-play est essentiel. Toute tentative de tricherie, de manipulation des résultats,
                ou tout comportement déloyal entraînera la disqualification du participant concerné.</p>

            <p><h3>Responsabilité Individuelle</h3></p>

            <p>En participant au concours, chaque participant accepte que son image (photos et vidéos)
                puisse être utilisée à des fins promotionnelles par les photographes et vidéastes présents lors de l'événement.</p>

            <p>Chaque participant est responsable de ses effets personnels et doit
                respecter les règles de sécurité énoncées dans le règlement.</p>
